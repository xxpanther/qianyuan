package org.gxz.znrl.entity;

/**
 * Created by xieyt on 2016-10-20.
 * 加载数据库静态常量的实体bean
 */
public class ConstantEntity {
    public String key;
    public String value;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
