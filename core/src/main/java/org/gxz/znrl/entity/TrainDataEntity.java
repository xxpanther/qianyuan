public class TrainDataEntity {
    private String id;
    private String carOrder;
    private String carType;
    private String carNo;
    private String tickQty;
    private String pzQty;
    private String mzQty;
    private String netQty;
    private String weightTime;
    private String weightSpeed;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCarOrder() {
        return carOrder;
    }

    public void setCarOrder(String carOrder) {
        this.carOrder = carOrder;
    }

    public String getCarType() {
        return carType;
    }

    public void setCarType(String carType) {
        this.carType = carType;
    }

    public String getCarNo() {
        return carNo;
    }

    public void setCarNo(String carNo) {
        this.carNo = carNo;
    }

    public String getTickQty() {
        return tickQty;
    }

    public void setTickQty(String tickQty) {
        this.tickQty = tickQty;
    }

    public String getPzQty() {
        return pzQty;
    }

    public void setPzQty(String pzQty) {
        this.pzQty = pzQty;
    }

    public String getMzQty() {
        return mzQty;
    }

    public void setMzQty(String mzQty) {
        this.mzQty = mzQty;
    }

    public String getNetQty() {
        return netQty;
    }

    public void setNetQty(String netQty) {
        this.netQty = netQty;
    }

    public String getWeightTime() {
        return weightTime;
    }

    public void setWeightTime(String weightTime) {
        this.weightTime = weightTime;
    }

    public String getWeightSpeed() {
        return weightSpeed;
    }

    public void setWeightSpeed(String weightSpeed) {
        this.weightSpeed = weightSpeed;
    }
}
